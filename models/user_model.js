const { DataTypes } = require('sequelize');
const { sequelize } = require('../models/bundle_model'); // Menggunakan objek sequelize dari bundle_model.js

const User = sequelize.define('User', {
  username: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true,
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true,
  },
  password: {
    type: DataTypes.STRING,
    allowNull: false,
  },
});

module.exports = User;
